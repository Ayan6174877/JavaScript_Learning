// function can be mostly defined in three types

// 1 normal function

function sum(a, b){
    let result = a + b;
    return result;
}

// 2 binding function

const sun = function(){
    let result = a + b;
    return result;
}

// 3 arrow functions

sum = (a, b) => {
    let result = a + b;
    return result;
};
